package ajn.zhihu.zhuanlan.chapter17;

public class Demo01 {

	public static void main(String[] args) {
		System.out.println(Math.abs(-12.3));				// print: 12.3
		System.out.println(Math.max(12, 32));				// print: 32
		System.out.println(Math.ceil(1.111));				// print: 2.0
		System.out.println(Math.random());					// print: 0.10120667368676317
		System.out.println(Math.sin(Math.PI/2));			// print: 1.0
		System.out.println(Math.cos(Math.toRadians(180)));	// print: -1.0
		System.out.println(Math.pow(2, 3));					// print: 8.0
		System.out.println(Math.sqrt(4));					// print: 2.0
		System.out.println(Math.cbrt(8));					// print: 2.0
		System.out.println(Math.log(Math.E));				// print: 1.0
	}

}
