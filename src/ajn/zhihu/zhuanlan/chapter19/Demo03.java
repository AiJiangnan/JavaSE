package ajn.zhihu.zhuanlan.chapter19;

public class Demo03 {
	public static void main(String[] args) {
		System.out.println(Float.MAX_EXPONENT);              // print: 127 
		System.out.println(Float.MIN_EXPONENT);              // print: -126 
		System.out.println(Float.MAX_VALUE);                 // print: 3.4028235E38 
		System.out.println(Float.MIN_VALUE);                 // print: 1.4E-45 
		System.out.println(Float.NaN);                       // print: NaN 
		System.out.println(Float.NEGATIVE_INFINITY);         // print: -Infinity 
		System.out.println(Float.POSITIVE_INFINITY);         // print: Infinity 
		System.out.println(Float.SIZE);                      // print: 32 
	}
}
