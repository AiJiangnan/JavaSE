package ajn.zhihu.zhuanlan.chapter5;

public class Demo02 {
	public static void main(String[] args) {
		int a = 6;
		int b = 2;
		System.out.println(""
				+ (a+b) + "\n"			//print: 8
				+ (a-b) + "\n"			//print: 4
				+ (a*b) + "\n"			//print: 12
				+ (a/b) + "\n"			//print: 3
				+ (a%b) + "\n"			//print: 0
				+ (++a) + "\n"			//print: 7
				+ (a++) + "\n"			//print: 7
				+ (--b) + "\n"			//print: 1
				+ (b--)	+ "");			//print: 1
	}
}
